def verif_nb(nb) :
    nb = str(nb)
    a = []
    b = 0
    nb = nb + '0'
    for i in range(4) :
        a.append(int(nb[i*2])*3)
        a.append(int(nb[i*2+1])*1)
    for i in range(len(a)) :
        b += a[i]
    c = b % 10
    d = 10-c
    if d == 10 :
        return nb
    elif d <= 9 :
        nb = nb[0:7] + str(d)
        return nb

def trad(nb,dict) :
    a = ''
    nb = str(nb)
    for i in range(len(nb)) :
        a=a+(dict[int(nb[i])]) 
    return a

def nb_to_bincode(nb) :
    g = {
    0: "0001101",
    1: "0011001",
    2: "0010011",
    3: "0111101",
    4: "0100011",
    5: "0110001",
    6: "0101111",
    7: "0111011",
    8: "0110111",
    9: "0001011"
    }

    d = {
    0: "1110010",
    1: "1100110",
    2: "1101100",
    3: "1000010",
    4: "1011100",
    5: "1001110",
    6: "1010000",
    7: "1000100",
    8: "1001000",
    9: "1110100"
    }
    nb = str(nb)
    a = nb[:4]
    b = nb[4:]
    at = trad(a,g)
    bt = trad(b,d)
    return '101' + at + '010101' + bt + '101'


